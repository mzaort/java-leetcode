package solutionExtra;

public class Activity {
	public int start;
	public int end;
	public int value;
	public Activity(int start, int end, int value) {
		super();
		this.start = start;
		this.end = end;
		this.value = value;
	}
	public Activity(int start, int end) {
		super();
		this.start = start;
		this.end = end;
	}	
}
